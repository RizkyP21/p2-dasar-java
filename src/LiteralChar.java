public class LiteralChar {
    public static void main(String[] args) {
        char tab='\t';
        char newline='\n';
        char singleQuote='\'';
        char doubleQuote='\"';
        System.out.println("Ini contoh"+tab+"literal tab");
        System.out.println("Ini contoh"+newline+"literal newline");
        System.out.println(""+singleQuote+"Ini contoh literal single quote"+"SingleQuote");
        System.out.println(""+doubleQuote+"Ini contoh literal double quote"+"doubleQuote");
    }

}
